<p>Sistem Pendidikan dan pengajaran pada dasarnya mengikuti 
ketentuan-ketentuan yang telah ditetapkan <b>Departemen pendidikan Nasional 
Republik Indonesia.</b> 
Sistem pendidikan USNI, sesuai dengan peraturan pemerintah menggunakan 
<b>sistem kredit semester (SKS)</b>. Setiap semester efektif  berlangsung 
selama 16 minggu dan Kuliah diberikan pada pagi, siang dan sore hari.</p>

